    public void showCTHDWindow(String maHD) {
        JFrame frame = new JFrame("Chi tiết hóa đơn");
        JTable table = new JTable();
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.addColumn("Sản phẩm"); // Thêm các cột cần thiết
        tableModel.addColumn("Số lượng");
        tableModel.addColumn("Đơn giá");
        tableModel.addColumn("Thành tiền");

        String sql = "SELECT sanpham.tenSP, SoLuong, chitiethoadon.DonGia, ThanhTien FROM chitiethoadon, sanpham WHERE chitiethoadon.maSP = sanpham.maSP and maHD = ?";

        try (PreparedStatement pstmt = connection.prepareStatement(sql)) {
            pstmt.setString(1, maHD);
            try (ResultSet rs = pstmt.executeQuery()) {
                while (rs.next()) {
                    Object[] row = {
                        rs.getString("tenSP"),
                        rs.getString("SoLuong"),
                        rs.getString("DonGia"),
                        rs.getString("ThanhTien")
                    };
                    tableModel.addRow(row);
                }
            }

            table.setModel(tableModel);
            JScrollPane scrollPane = new JScrollPane(table);
            frame.add(scrollPane);
            frame.setSize(400, 250); // Kích thước cửa sổ
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
            frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE); // Đảm bảo cửa sổ được đóng khi nhấn nút đóng

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

