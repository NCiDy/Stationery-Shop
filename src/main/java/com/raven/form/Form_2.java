    public void update_table() throws SQLException {
        String userID = Login.getUserId();
        String sql = null;
        tableModel = (DefaultTableModel) TB_HD.getModel();
        tableModel.setRowCount(0);

        if (userID != null) {
            if (userID.startsWith("AD")) {
                sql = "SELECT maHD, NgayThanhToan, TongTien FROM hoadon";
                ResultSet rs = repo.executeQuery(sql);
                try {
                    while (rs.next()) {
                        Object[] row = {
                            rs.getString("maHD"),
                            rs.getString("NgayThanhToan"),
                            rs.getString("TongTien")
                        };
                        tableModel.addRow(row);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else if (userID.startsWith("NV")) {
                sql = "SELECT maHD, NgayThanhToan, TongTien FROM hoadon WHERE maNV = ?";
                try (PreparedStatement pstmt = connection.prepareStatement(sql)) {
                    pstmt.setString(1, Login.getUserId());
                    try (ResultSet rs = pstmt.executeQuery()) {
                        while (rs.next()) {
                            Object[] row = {
                                rs.getString("maHD"),
                                rs.getString("NgayThanhToan"),
                                rs.getString("TongTien")
                            };
                            tableModel.addRow(row);
                        }
                    }
                }
            } else {
                sql = "SELECT maHD, NgayThanhToan, TongTien FROM hoadon WHERE SDT = ?";
                try (PreparedStatement pstmt = connection.prepareStatement(sql)) {
                    pstmt.setString(1, Login.getUserId());
                    try (ResultSet rs = pstmt.executeQuery()) {
                        while (rs.next()) {
                            Object[] row = {
                                rs.getString("maHD"),
                                rs.getString("NgayThanhToan"),
                                rs.getString("TongTien")
                            };
                            tableModel.addRow(row);
                        }
                    }
                }
            }
        }
    }

