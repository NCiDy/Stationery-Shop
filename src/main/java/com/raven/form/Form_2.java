    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String tenHD = maHD.getText();
        try {
            // Thực hiện câu truy vấn SQL để tìm kiếm sản phẩm
            String searchQuery = "SELECT maHD, NgayThanhToan, TongTien "
                    + "FROM HoaDon "
                    + "WHERE maHD = ?";

            PreparedStatement searchStmt = repo.getConnection().prepareStatement(searchQuery);
            searchStmt.setString(1, tenHD);

            ResultSet rs = searchStmt.executeQuery();

            // Xóa dữ liệu cũ khỏi tableModel
            ((DefaultTableModel) TB_HD.getModel()).setRowCount(0);

            // Thêm dữ liệu tìm được vào tableModel
            while (rs.next()) {
                String maSP = rs.getString("maHD");
                String ngayThanhToan = rs.getString("NgayThanhToan");
                String tongTien = rs.getString("TongTien");

                // Thêm dữ liệu vào tableModel
                ((DefaultTableModel) TB_HD.getModel()).addRow(new Object[]{maSP, ngayThanhToan, tongTien});
            }

            // Cập nhật UI của table
            TB_HD.repaint();
            TB_HD.updateUI();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Lỗi khi tìm kiếm theo mã hóa đơn: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

