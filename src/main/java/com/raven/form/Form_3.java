    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mancc = txtMaNCC.getText();
        String ten = txtNCC.getText();
        String sdt = txtSDT.getText();
        String dc = txtDC.getText();

        // Xác nhận với người dùng
        int xacNhan = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn thêm nhà cung cấp?", "Xác nhận Thêm", JOptionPane.YES_NO_OPTION);

        if (xacNhan == JOptionPane.YES_OPTION) {
            try {

                String query = "INSERT INTO NhaCungCap (maNCC, tenNCC, SDT, diachi) "
                        + "VALUES (?, ?, ?, ?)";
                PreparedStatement stmt = repo.getConnection().prepareStatement(query);
                stmt.setString(1, mancc);
                stmt.setString(2, ten);
                stmt.setString(3, sdt);
                stmt.setString(4, dc);

                int rowsAffected1 = stmt.executeUpdate();

                if (rowsAffected1 > 0) {
                    // Cập nhật lại dữ liệu trong tableModel
                    update_table();
                    JOptionPane.showMessageDialog(null, "Đã thêm sản phẩm thành công!");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Lỗi khi thêm nhà cung cấp: " + e.getMessage());
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        // TODO add your handling code here:
        txtMaNCC.setEditable(false);
        String mancc = txtMaNCC.getText();
        String ten = txtNCC.getText();
        String sdt = txtSDT.getText();
        String dc = txtDC.getText();

        // Xác nhận với người dùng
        int xacNhan = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn sửa thông tin nhà cung cấp?", "Xác nhận Sửa", JOptionPane.YES_NO_OPTION);

        if (xacNhan == JOptionPane.YES_OPTION) {
            try {
                String query = "UPDATE NhaCungCap "
                        + "SET tenNCC = ?, SDT = ?, diachi = ? "
                        + "WHERE maNCC = ?";
                PreparedStatement stmt = repo.getConnection().prepareStatement(query);
                stmt.setString(1, ten);
                stmt.setString(2, sdt);
                stmt.setString(3, dc);
                stmt.setString(4, mancc);

                int rowsAffected = stmt.executeUpdate();

                if (rowsAffected > 0) {
                    // Cập nhật lại dữ liệu trong tableModel
                    update_table();
                    JOptionPane.showMessageDialog(null, "Đã sửa thông tin nhà cung cấp thành công!");
                } else {
                    JOptionPane.showMessageDialog(null, "Không tìm thấy nhà cung cấp với mã " + mancc + " để sửa.");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Lỗi khi sửa thông tin nhà cung cấp: " + e.getMessage());
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_EditActionPerformed

