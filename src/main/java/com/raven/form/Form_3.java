    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        txtMaNCC.setEditable(true);
        txtNCC.setEditable(false);
        txtSDT.setEditable(false);
        txtDC.setEditable(false);
        String maNCC = txtMaNCC.getText();

        try {
            String query = "SELECT * FROM NhaCungCap "
                    + "WHERE maNCC = ?";
            PreparedStatement stmt = repo.getConnection().prepareStatement(query);
            stmt.setString(1, maNCC);

            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                // Lấy dữ liệu từ ResultSet và hiển thị lên các trường tương ứng
                txtMaNCC.setText(rs.getString("maNCC"));
                txtNCC.setText(rs.getString("tenNCC"));
                txtDC.setText(rs.getString("diaChi"));
                txtSDT.setText(rs.getString("sdt"));
            } else {
                JOptionPane.showMessageDialog(null, "Không tìm thấy nhà cung cấp với mã " + maNCC);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Lỗi khi tìm kiếm nhà cung cấp: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchActionPerformed

